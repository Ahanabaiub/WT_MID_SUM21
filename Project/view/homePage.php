    <h2>Welcome to RDBMS College</h2>
    <p>
        RDBMS is one of the most prestigious and important educational institutions in Dhaka. It is located in the heart of the Dhaka Cantonment,
        comprising of an area of 5.00 acres of land. RDBMS was established in 1960 following the curriculum of British Public College. A British educationist 
        Mr. Dolman was the first principal of the School. The classes started functioning with only six classes, from class Four to Nine. The first batch of students appeared in the 
        Matriculation Examination in 1962. Subsequently, with further expansion of the institution, it was converted into a college and was renamed as RDBMS College.
        In 1995, for some administrative reasons and effective teaching, the school section was totally bifurcated and shifted to its present location and its original name 
        ''RDBMS College, Dhaka'' was reinstated. The school is housed in two buildings: six storied and five storied. At present this is a two-shift school with the
        strength of about 7,500 students. The Day Shift which was discontinued, has been reintroduced on 1st January 2013.
